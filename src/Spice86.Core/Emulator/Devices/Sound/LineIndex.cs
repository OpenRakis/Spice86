namespace Spice86.Core.Emulator.Devices.Sound;

/// <summary>
///     A simple enum to describe the array index associated with a given audio line.
/// </summary>
public enum LineIndex {
    /// <summary>
    ///     Left audio channel (index 0).
    /// </summary>
    Left = 0,

    /// <summary>
    ///     Right audio channel (index 1).
    /// </summary>
    Right = 1
}
