    }

    /// <summary>
    ///     Builds a 256-entry ARGB palette from a flat byte array of 6-bit RGB triplets.
    /// </summary>
    /// <param name="paletteRgb6">A flat array of R, G, B triplets (6-bit each). Length must be at least <paramref name="colorCount"/> × 3.</param>
    /// <param name="colorCount">Number of colors to convert.</param>
    /// <returns>Array of ARGB uint values.</returns>
    public static uint[] BuildArgbPalette(ReadOnlySpan<byte> paletteRgb6, int colorCount) {
        uint[] argb = new uint[colorCount];
        for (int i = 0; i < colorCount; i++) {
            int offset = i * 3;
            if (offset + 2 >= paletteRgb6.Length) {
                break;
            }
            byte r6 = paletteRgb6[offset];
            byte g6 = paletteRgb6[offset + 1];
            byte b6 = paletteRgb6[offset + 2];
            uint r8 = (uint)(r6 << 2 | r6 >> 4);
            uint g8 = (uint)(g6 << 2 | g6 >> 4);
            uint b8 = (uint)(b6 << 2 | b6 >> 4);
            argb[i] = 0xFF000000U | (r8 << 16) | (g8 << 8) | b8;
        }
        return argb;
    }

    /// <summary>
    ///     Gets the standard VGA 256-color default palette as a 256-entry ARGB array.
    /// </summary>
    public static uint[] DefaultVga256Palette => BuildArgbPalette(Palettes.Vga, 256);

    /// <summary>
    ///     Gets the standard CGA 16-color palette as a 16-entry ARGB array.
    /// </summary>
    public static uint[] DefaultCga16Palette => BuildArgbPalette(Palettes.Cga, 16);

    /// <summary>
    ///     Gets the standard EGA 16-color palette as a 16-entry ARGB array.
    /// </summary>
    public static uint[] DefaultEga16Palette => BuildArgbPalette(Palettes.Ega, 16);

    /// <summary>
    ///     Gets a monochrome 2-color palette (black and white).
    /// </summary>
    public static uint[] MonochromePalette => [0xFF000000, 0xFFFFFFFF];

    /// <summary>
    ///     Renders a memory region as a bitmap using the specified video mode.
    /// </summary>
    /// <param name="data">The raw memory bytes to interpret.</param>
    /// <param name="width">The width of the output bitmap in pixels.</param>
    /// <param name="height">The height of the output bitmap in pixels.</param>
    /// <param name="mode">The video mode to use for interpretation.</param>
    /// <param name="palette">The ARGB palette. If null, a default palette for the mode is used.</param>
    /// <returns>A uint array of ARGB pixel data (width × height), or an empty array if inputs are invalid.</returns>
    public static uint[] Render(ReadOnlySpan<byte> data, int width, int height, MemoryBitmapVideoMode mode, uint[]? palette) {
        if (width <= 0 || height <= 0 || data.Length == 0) {
            return [];
        }
        return mode switch {
            MemoryBitmapVideoMode.Raw8Bpp => RenderRaw8Bpp(data, width, height, palette ?? DefaultVga256Palette),
            MemoryBitmapVideoMode.Vga256Color => RenderVga256(data, width, height, palette ?? DefaultVga256Palette),
            MemoryBitmapVideoMode.Ega16Color => RenderEga16(data, width, height, palette ?? DefaultEga16Palette),
            MemoryBitmapVideoMode.Cga4Color => RenderCga4(data, width, height, palette ?? DefaultCga16Palette),
            MemoryBitmapVideoMode.Cga2Color => RenderCga2(data, width, height, palette ?? MonochromePalette),
            MemoryBitmapVideoMode.Text => RenderText(data, width, height, palette ?? DefaultCga16Palette),
            _ => []
        };
    }

    /// <summary>
    ///     Raw 8bpp: each byte is a palette index, pixels are laid out linearly.
    /// </summary>
    private static uint[] RenderRaw8Bpp(ReadOnlySpan<byte> data, int width, int height, uint[] palette) {
        int pixelCount = width * height;
        uint[] pixels = new uint[pixelCount];
        int bytesAvailable = Math.Min(data.Length, pixelCount);
        for (int i = 0; i < bytesAvailable; i++) {
            int index = data[i] % palette.Length;
            pixels[i] = palette[index];
        }
        // Fill remaining pixels with black if data is shorter than bitmap.
        for (int i = bytesAvailable; i < pixelCount; i++) {
            pixels[i] = 0xFF000000;
        }
        return pixels;
    }

    /// <summary>
    ///     VGA 256-color (Mode 13h): linear 320×200, each byte = palette index.
    ///     Same as Raw8Bpp but conceptually distinct as it represents Mode 13h.
    /// </summary>
    private static uint[] RenderVga256(ReadOnlySpan<byte> data, int width, int height, uint[] palette) {
        return RenderRaw8Bpp(data, width, height, palette);
    }

    /// <summary>
    ///     EGA 16-color planar: 4 bit planes, each bit in a byte maps to one pixel.
    ///     Plane layout: for a given byte offset, plane 0 is at offset, plane 1 at offset + planeSize, etc.
    ///     Each byte in a plane represents 8 consecutive pixels (MSB = leftmost).
    /// </summary>
    private static uint[] RenderEga16(ReadOnlySpan<byte> data, int width, int height, uint[] palette) {
        int pixelCount = width * height;
        uint[] pixels = new uint[pixelCount];
        int bytesPerRow = (width + 7) / 8;
        int planeSize = bytesPerRow * height;

        for (int y = 0; y < height; y++) {
            for (int xByte = 0; xByte < bytesPerRow; xByte++) {
                int byteOffset = y * bytesPerRow + xByte;
                byte plane0 = SafeRead(data, byteOffset);
                byte plane1 = SafeRead(data, byteOffset + planeSize);
                byte plane2 = SafeRead(data, byteOffset + planeSize * 2);
                byte plane3 = SafeRead(data, byteOffset + planeSize * 3);

                for (int bit = 7; bit >= 0; bit--) {
                    int x = xByte * 8 + (7 - bit);
                    if (x >= width) {
                        break;
                    }
                    int colorIndex = ((plane3 >> bit) & 1) << 3 |
                                     ((plane2 >> bit) & 1) << 2 |
                                     ((plane1 >> bit) & 1) << 1 |
                                     ((plane0 >> bit) & 1);
                    int destIndex = y * width + x;
                    pixels[destIndex] = palette[colorIndex % palette.Length];
                }
            }
        }
        return pixels;
    }

    /// <summary>
    ///     CGA 4-color mode: 2 bits per pixel, interleaved scanlines.
    ///     Even scanlines in first half of data, odd scanlines in second half.
    ///     Each byte contains 4 pixels (MSB pair = leftmost pixel).
    /// </summary>
    private static uint[] RenderCga4(ReadOnlySpan<byte> data, int width, int height, uint[] palette) {
        int pixelCount = width * height;
        uint[] pixels = new uint[pixelCount];
        int bytesPerRow = (width + 3) / 4;
        int bankSize = data.Length / 2;

        for (int y = 0; y < height; y++) {
            int bankOffset = (y & 1) == 0 ? 0 : bankSize;
            int rowInBank = y / 2;

            for (int xByte = 0; xByte < bytesPerRow; xByte++) {
                int byteOffset = bankOffset + rowInBank * bytesPerRow + xByte;
                byte dataByte = SafeRead(data, byteOffset);

                for (int pixel = 0; pixel < 4; pixel++) {
                    int x = xByte * 4 + pixel;
                    if (x >= width) {
                        break;
                    }
                    int shift = 6 - pixel * 2;
                    int colorIndex = (dataByte >> shift) & 0x03;
                    int destIndex = y * width + x;
                    pixels[destIndex] = palette[colorIndex % palette.Length];
                }
            }
        }
        return pixels;
    }

    /// <summary>
    ///     CGA 2-color (hi-res mono) mode: 1 bit per pixel, interleaved scanlines.
    ///     Even scanlines in first half of data, odd scanlines in second half.
    ///     Each byte contains 8 pixels (MSB = leftmost).
    /// </summary>
    private static uint[] RenderCga2(ReadOnlySpan<byte> data, int width, int height, uint[] palette) {
        int pixelCount = width * height;
        uint[] pixels = new uint[pixelCount];
        int bytesPerRow = (width + 7) / 8;
        int bankSize = data.Length / 2;

        for (int y = 0; y < height; y++) {
            int bankOffset = (y & 1) == 0 ? 0 : bankSize;
            int rowInBank = y / 2;

            for (int xByte = 0; xByte < bytesPerRow; xByte++) {
                int byteOffset = bankOffset + rowInBank * bytesPerRow + xByte;
                byte dataByte = SafeRead(data, byteOffset);

                for (int bit = 7; bit >= 0; bit--) {
                    int x = xByte * 8 + (7 - bit);
                    if (x >= width) {
                        break;
                    }
                    int colorIndex = (dataByte >> bit) & 1;
                    int destIndex = y * width + x;
                    pixels[destIndex] = palette[colorIndex % palette.Length];
                }
            }
        }
        return pixels;
    }

    /// <summary>
    ///     Text mode rendering: each pair of bytes is (character code, attribute).
    ///     Characters are rendered using an 8×16 built-in font.
    ///     The attribute byte encodes foreground (bits 0-3) and background (bits 4-7) colors.
    ///     Width is in characters (each character is 8 pixels wide), height is in characters (each 16 pixels tall).
    /// </summary>
    private static uint[] RenderText(ReadOnlySpan<byte> data, int widthChars, int heightChars, uint[] palette) {
        int pixelWidth = widthChars * 8;
        int pixelHeight = heightChars * 16;
        int pixelCount = pixelWidth * pixelHeight;
        uint[] pixels = new uint[pixelCount];

        for (int charY = 0; charY < heightChars; charY++) {
            for (int charX = 0; charX < widthChars; charX++) {
                int dataOffset = (charY * widthChars + charX) * 2;
                byte charCode = SafeRead(data, dataOffset);
                byte attribute = SafeRead(data, dataOffset + 1);

                uint fgColor = palette[(attribute & 0x0F) % palette.Length];
                uint bgColor = palette[((attribute >> 4) & 0x0F) % palette.Length];

                int fontOffset = charCode * 16;
                for (int row = 0; row < 16; row++) {
                    byte fontByte = VgaFont8X16[fontOffset + row];
                    int pixelY = charY * 16 + row;
                    if (pixelY >= pixelHeight) {
                        break;
                    }
                    for (int col = 0; col < 8; col++) {
                        int pixelX = charX * 8 + col;
                        if (pixelX >= pixelWidth) {
                            break;
                        }
                        bool isForeground = (fontByte & (0x80 >> col)) != 0;
                        int destIndex = pixelY * pixelWidth + pixelX;
                        pixels[destIndex] = isForeground ? fgColor : bgColor;
                    }
                }
            }
        }
        return pixels;
    }

    /// <summary>
    ///     Safely reads a byte from the data span, returning 0 if the offset is out of range.
    /// </summary>
    private static byte SafeRead(ReadOnlySpan<byte> data, int offset) {
        if (offset < 0 || offset >= data.Length) {
            return 0;
        }
        return data[offset];
    }

    /// <summary>
    ///     Gets the output bitmap width in pixels for the given mode and input character/pixel width.
    /// </summary>
    /// <param name="mode">The video mode.</param>
    /// <param name="inputWidth">The width value (pixels for graphics modes, characters for text mode).</param>
    /// <returns>The output bitmap width in pixels.</returns>
    public static int GetOutputPixelWidth(MemoryBitmapVideoMode mode, int inputWidth) {
        return mode == MemoryBitmapVideoMode.Text ? inputWidth * 8 : inputWidth;
    }

    /// <summary>
    ///     Gets the output bitmap height in pixels for the given mode and input character/pixel height.
    /// </summary>
    /// <param name="mode">The video mode.</param>
    /// <param name="inputHeight">The height value (pixels for graphics modes, characters for text mode).</param>
    /// <returns>The output bitmap height in pixels.</returns>
    public static int GetOutputPixelHeight(MemoryBitmapVideoMode mode, int inputHeight) {
        return mode == MemoryBitmapVideoMode.Text ? inputHeight * 16 : inputHeight;
    }
